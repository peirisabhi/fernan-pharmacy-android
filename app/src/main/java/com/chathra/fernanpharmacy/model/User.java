package com.chathra.fernanpharmacy.model;

import java.util.Date;

public class User {
    int id;
    String type;

    public User(int id, String type) {
        this.id = id;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
